namespace VS22_ConsoleApp.Utility
{
    /// <summary>
    /// A utility class for console-related operations.
    /// </summary>
    public static class U_Console
    {
        /// <summary>
        /// Prints the specified string to the console followed by a newline.
        /// </summary>
        /// <param name="input">The string to be printed to the console.</param>
        public static void Print(string input) => Console.WriteLine(input);
    }
}
